/// Model class for package info
class PackageInfoData {
  String? appName;
  String? versionName;
  String? packageName;
  String? versionCode;
  int? androidSDKVersion;

  PackageInfoData({
    this.appName,
    this.versionName,
    this.packageName,
    this.versionCode,
    this.androidSDKVersion,
  });
}
